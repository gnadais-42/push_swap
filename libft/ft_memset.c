#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ss;
	unsigned char	cc;

	i = 0;
	ss = (unsigned char *) s;
	cc = (unsigned char) c;
	while (i < n)
		ss[i++] = cc;
	return (s);
}
