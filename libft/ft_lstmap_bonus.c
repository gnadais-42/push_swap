#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*map;
	t_list	*node;
	void	*fcontent;

	if (!f || !del)
		return (NULL);
	map = NULL;
	while (lst)
	{
		fcontent = f(lst->content);
		node = ft_lstnew(fcontent);
		if (!node)
		{
			del(fcontent);
			ft_lstclear(&map, del);
			return (NULL);
		}
		ft_lstadd_back(&map, node);
		lst = lst->next;
	}
	return (map);
}
